/*
 * 6.- Crea un array de 5 elementos cuyos valores sean nombres de personas. Imprime los elementos 
del array a través de un bucle for
 */
public class seis {
    public static void main(String[] args) {
        String[] nombresPersonas = {"Carlos", "Candela", "Carla", "Carmen", "Cándido"};
        for (int i = 0; i<nombresPersonas.length; i++){
            System.out.println(nombresPersonas[i]);
        }
    }
}
